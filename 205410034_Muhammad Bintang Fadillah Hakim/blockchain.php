      <h3 class="fw-light">Apa itu blockchain</h3>
      <p>
        Dilansir dari buku Blockchain for Dummies oleh Manav Gupta dijelaskan,
        blockchain mulanya dikembangkan untuk memenuhi kebutuhan akan sistem
        yang efisien, hemat biaya, andal, dan aman untuk melakukan dan mencatat
        sebuah transaksi keuangan. Blockchain mulanya digunakan pada bitcoin
        yang dikembangkan pada tahun 2009 oleh Satoshi Nakamoto. Tak seperti
        uang tradisional yang diterbitkan oleh bank sentral, bitcoin tidak
        memiliki otoritas sentral dan tidak ada pihak yang mengontrol. Ketimbang
        bergantung pada otoritas sentral untuk mengawasi, melakukan verifikasi
        dan menyetujui transaksi serta mengelola pasokan uang, bitcoin
        diaktifkan oleh jaringan komputer peer to peer. Dilansir dari
        Investopedia, blockchain adalah sebuah pusat data terdistribusi yang
        dibagikan pada setiap titik yang ada pada jaringan komputer. Sebagai
        data base atau pusat data, blockchain berisi beragam informasi dalam
        format digital. Informasi yang ada di dalamnya tergantung pada jenis
        penggunaan blockchain. Misalnya saja pada bitcoin, informasi yang
        terdapat di dalamnya terkait dengan detil transaksi, jumlah koin,
        pemilih koin, hingga penerima koin. Dengan demikian, blockchain
        berfungsi untuk menjaga catatan mengenai transaksi yang aman dan
        terdesentralisasi. Apa yang membedakan blockchain dengan pendataan
        konvensional? Seperti yang dijelaskan sebelumnya, sistem blockchain
        membuat sebuah catatan transaksi menjadi terdesentralisasi. Blockchain
        adalah sebuah buku kas besar (ledger) yang terdistribusi. Teknologi
        inilah yang membuat pencatatan terjadi di banyak komputer atau yang
        dikenal dengan nodes atau simpul. Setiap pengguna blockchain bisa
        menjadi simpul-simpul yang saling terhubung, namun untuk
        pengoperasiannya, membuautuhkan tenaga komputer yang sangat besar.
        Simpul-simpul tersebutlah yang kemudian melakukan verifikasi,
        menyetujui, dan mencatatkan data di ledger.
